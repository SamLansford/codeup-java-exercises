package PBJ;

public class PeanutButter extends Ingredient {

    public void flavor() {
        System.out.println("The flavor of peanut butter tastes like peanuts");
    }

    public void color() {
        System.out.println("The color of peanut butter is yellow");
    }

    public void throwAway() {
        System.out.println("Throw away peanut butter");
    }
}
